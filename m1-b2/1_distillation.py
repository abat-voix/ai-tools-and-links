from openai import OpenAI
from dotenv import load_dotenv
import os

load_dotenv()

client = OpenAI(
    api_key=os.getenv("OPENAI_API_KEY")
)

dataset = [
    "Клиент недоволен доставкой",
    "Отличный сервис, спасибо!",
]

def get_teacher_logits(text):
    # Teacher (большая модель)
    response = client.responses.create(
        model="gpt-5.4",  # Teacher
        input=[{"role": "user", "content": f"Определи тональность: {text}"}],
        include=["message.output_text.logprobs", ],  # важно: получаем вероятности токенов
        top_logprobs=5,
    )
    return response.output[0].content[0].logprobs  # распределение вероятностей

# Собираем датасет soft labels
training_data = []
for text in dataset:
    probs = get_teacher_logits(text)
    training_data.append({
        "text": text,
        "soft_labels": probs,
    })

print(training_data)
