from openai import OpenAI
from dotenv import load_dotenv
import os

load_dotenv()

client = OpenAI(
    api_key=os.getenv("GROQ_API_KEY"),
    base_url="https://api.groq.com/openai/v1"
)

dataset = [
    "Клиент недоволен доставкой",
    "Отличный сервис, спасибо!",
]

def get_teacher_logits(text):
    # Teacher (большая модель)
    # Groq не поддерживает logprobs, поэтому просим модель вернуть уверенность в JSON
    import json
    response = client.chat.completions.create(
        model="llama-3.3-70b-versatile",  # Teacher (Groq)
        messages=[{"role": "user", "content": (
            f"Определи тональность текста. Ответь ТОЛЬКО в формате JSON: "
            f'{{"positive": 0.0, "negative": 0.0}} где значения — вероятности от 0 до 1, в сумме 1.\n'
            f"Текст: {text}"
        )}],
        response_format={"type": "json_object"},
    )
    return json.loads(response.choices[0].message.content)  # {"positive": 0.92, "negative": 0.08}

# Собираем датасет soft labels
training_data = []
for text in dataset:
    probs = get_teacher_logits(text)
    training_data.append({
        "text": text,
        "soft_labels": probs,
    })

print(training_data)
